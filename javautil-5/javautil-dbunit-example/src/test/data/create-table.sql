   COMMENT ON COLUMN "DM_FOLDER"."ADD_DOCUMENT_FLG" IS 'Specifies if Documents can be added to the Folder. Values are Y or N.';
 
   COMMENT ON COLUMN "DM_FOLDER"."DEL_DOCUMENT_FLG" IS 'Specifies if Documents can be deleted from the Folder. Values are Y or N.';
 
   COMMENT ON COLUMN "DM_FOLDER"."ADD_DOCUMENT_CONTENT_FLG" IS 'Specifies if content can be added to Documents in the Folder. Values are Y or N.';
 
   COMMENT ON COLUMN "DM_FOLDER"."DEL_DOCUMENT_CONTENT_FLG" IS 'Specifies if content can be deleted from Documents in the Folder. Values are Y or N.';
 
   COMMENT ON TABLE "DM_FOLDER"  IS 'This table stores all the Folders that will be created in the Document Management system.';
 

